// Пусть даны переменные a и b со сторонами прямоугольника. Найдите площадь прямоугольника и запишите ее в переменную s. 
// Выведите содержимое этой переменной на экран.

let a = prompt('Введите первую сторону прямоугольника');
let b = prompt('Введите вторую сторону прямоугольника');
let s = Number(a) * Number(b);
alert('Площадь прямоугольника = ' + s);